package ca.mss.rd.trade.exceptions;

public class WrongDataModelException extends RuntimeException {

	public WrongDataModelException() {
	}

	public WrongDataModelException(String arg0) {
		super(arg0);
	}

	public WrongDataModelException(Throwable arg0) {
		super(arg0);
	}

	public WrongDataModelException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
